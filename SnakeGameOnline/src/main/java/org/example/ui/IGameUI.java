package org.example.ui;

//интерфейс для взаимодействия между контроллером игры и пользовательским интерфейсом
public interface IGameUI {
    //запускает отображение пользовательского интерфейса
    void start();
    //вызывается когда игра начинается
    void onGameStarted();
    //вызывается когда игра завершается
    void onGameEnded();
    //вызывается при обновлении состояния игры для перерисовки
    void onGameStateUpdated();
    //вызывается при возникновении ошибки для отображения пользователю
    void onError(String message);
}
